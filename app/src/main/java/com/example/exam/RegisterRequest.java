package com.example.exam;

public class RegisterRequest {
    private String edName, edSurName, edEmail, edPass;

    public String getEdName() {
        return edName;
    }

    public void setEdName(String edName) {
        this.edName = edName;
    }

    public String getEdSurName() {
        return edSurName;
    }

    public void setEdSurName(String edSurName) {
        this.edSurName = edSurName;
    }

    public String getEdEmail() {
        return edEmail;
    }

    public void setEdEmail(String edEmail) {
        this.edEmail = edEmail;
    }

    public String getEdPass() {
        return edPass;
    }

    public void setEdPass(String edPass) {
        this.edPass = edPass;
    }
}
