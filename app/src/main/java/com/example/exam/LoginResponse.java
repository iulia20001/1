public class LoginResponse {

    private String user_id, edEmailLogin, edPassLogin;

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getEdEmailLogin() {
        return edEmailLogin;
    }

    public void setEdEmailLogin(String edEmailLogin) {
        this.edEmailLogin = edEmailLogin;
    }

    public String getEdPassLogin() {
        return edPassLogin;
    }

    public void setEdPassLogin(String edPassLogin) {
        this.edPassLogin = edPassLogin;
    }
}
